fn client(env: &Env) -> AidPassClaimClient<'_> {
    AidPassClaimClient::new(env, &env.register(AidPassClaim, ()))
}

#[test]
fn valid_proof_claims_once_and_double_claim_fails() {
    let env = Env::default();
    env.mock_all_auths();
    let client = client(&env);
    let admin = Address::generate(&env);
    let claimant = Address::generate(&env);
    let (vk, claim) = fixture(&env);
    let root = claim.public_signals.get(0).unwrap();

    client.init(&root, &admin, &vk);
    assert_eq!(client.claim(&claimant, &claim), true);
    assert_eq!(
        client.try_claim(&claimant, &claim),
        Err(Ok(AidPassError::NullifierSpent))
    );
}

#[test]
fn wrong_root_is_rejected_before_spend() {
    let env = Env::default();
    env.mock_all_auths();
    let client = client(&env);
    let admin = Address::generate(&env);
    let claimant = Address::generate(&env);
    let (vk, claim) = fixture(&env);
    let wrong_root = claim.public_signals.get(2).unwrap();

    client.init(&wrong_root, &admin, &vk);
    assert_eq!(
        client.try_claim(&claimant, &claim),
        Err(Ok(AidPassError::WrongRoot))
    );
}
